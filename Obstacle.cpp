Obstacle::Obstacle(void)
{
	xo = new int[2];
	xo[0] = rand()%640;
	xo[1] = 100;
}

Obstacle::Obstacle(const Obstacle &model)
{
	xo = new int[2];
	xo[0] = model.GetXo()[0];
	xo[1] = model.GetXo()[1];
}

// ===========================================================================
//                                  Destructor
// ===========================================================================
Obstacle::~Obstacle(void)
{
	delete xo;
}

// ===========================================================================
//                                 Public Methods
// ===========================================================================
int* Obstacle::GetXo(void) const
{
	return xo;
}

